n=int(input())
i=1
while i<=n:
    s=1
    while s<=(n-i):
        print(" ",end="")
        s+=1
    j=1
    val=i
    while j<=i:
        print(val,end="")
        val+=1
        j+=1
    k=1
    val=2*i-2
    while k<=i-1:
        print(val,end="")
        val-=1
        k+=1
    print()
    i+=1
